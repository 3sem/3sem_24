#include <math.h>
#include "math_funcs.h"

double square(double x)
{
    return x * x;
}

double root(double x)
{
    return sqrt(x);
}

double linear(double x)
{
    return 4 * x + 11;
}

double mnogochlen(double x)
{
    return 4 * x * x - 3 * x + 10;
}

double expon(double x)
{
    return exp2(x);
}